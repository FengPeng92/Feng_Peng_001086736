    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedrow = viewTable.getSelectedRow();
        if (selectedrow >= 0) {
            VitalSign vs = (VitalSign)viewTable.getValueAt(selectedrow, 0);
            temperatureText.setText(String.valueOf(vs.getTemperature()));
            bloodText.setText(String.valueOf(vs.getBloodPressure()));
            dateText.setText(vs.getDate());
            pulseText.setText(String.valueOf(vs.getPulse()));
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
            
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = viewTable.getSelectedRow();
        if (selectedrow >= 0) {
            VitalSign vs = (VitalSign)viewTable.getValueAt(selectedrow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null, "Vital sign has been deleted");
            temperatureText.setText("");
            bloodText.setText("");
            dateText.setText("");
            pulseText.setText("");
            populateTable();
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


