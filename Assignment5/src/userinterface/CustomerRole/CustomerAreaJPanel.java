    public void populateTableRes(Restaurant searchedRes) {
        DefaultTableModel dtm = (DefaultTableModel) tableRes.getModel();
        dtm.setRowCount(0);
        
        Object[] row = new Object[4];
        row[0] = searchedRes.getResName();
        row[1] = searchedRes.getResManager();
        row[2] = searchedRes.getAddress();
        row[3] = searchedRes.getPhone();
        dtm.addRow(row);
    }
    
    public void populateAllRes() {
        DefaultTableModel dtm = (DefaultTableModel) tableRes.getModel();
        dtm.setRowCount(0);
        
        for (Restaurant res : ecosystem.getRestaurantDirectory().getRestaurantList()) {
            Object[] row = new Object[4];
            row[0] = res.getResName();
            row[1] = res.getResManager();
            row[2] = res.getAddress();
            row[3] = res.getPhone();
            dtm.addRow(row);
        }
    }
    
    public void populateMenu(Restaurant selectedRes) {
        DefaultTableModel dtm = (DefaultTableModel) tableMenu.getModel();
        dtm.setRowCount(0);
        
        for (MenuItem item : selectedRes.getMenus()) {
            Object[] row = new Object[3];
            row[0] = item.getDishName();
            row[1] = item.getDescription();
            row[2] = item.getPrice();
            dtm.addRow(row);
        }
    }

    
