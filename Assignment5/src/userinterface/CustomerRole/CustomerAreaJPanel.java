    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String resName = txtRes.getText();
        if (resName.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter restaurant name.");
        } else {
            Restaurant searchedRes = ecosystem.getRestaurantDirectory().searchResByName(resName);
            if (searchedRes == null) {
                JOptionPane.showMessageDialog(null, "No searched result");
                return;
            } else {
                populateTableRes(searchedRes);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

