    private void btnViewMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewMenuActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableRes.getSelectedRow();
        
        if (selectedRow >= 0) {
            String resName = (String)tableRes.getValueAt(selectedRow, 0);
            selectedRes = ecosystem.getRestaurantDirectory().searchResByName(resName);
            populateMenu(selectedRes);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnViewMenuActionPerformed

    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tableMenu.getSelectedRow();
            int number = Integer.parseInt(txtNumber.getText());
            if (selectedRow >= 0) {
                String itemName = (String)tableMenu.getValueAt(selectedRow, 0);
               
                MenuItem selectItem = selectedRes.searchItemByName(itemName);
                OrderItem orderItem = new OrderItem(selectItem, number);
                String orderId = selectedRes.getResName() + " " + selectedRes.getOrders().size() + 1;
                Order newOrder = new Order(orderId, customer.getUsername(), selectedRes.getResName(), orderItem);
                customer.getOrders().add(newOrder);
                selectedRes.getOrders().add(newOrder);
                JOptionPane.showMessageDialog(null, "Place order successfully.");

            } else {
                JOptionPane.showMessageDialog(null, "Please select a row.");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Number must be integer");
        }
        
        
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

