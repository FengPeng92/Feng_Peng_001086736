    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableCustomers.getSelectedRow();
        
        if (selectedRow >= 0) {
            String username = (String) tableCustomers.getValueAt(selectedRow, 1);
            String name = (String)tableCustomers.getValueAt(selectedRow, 0);
            
            if (ecosystem.getUserAccountDirectory().remvoveUserAccountbyName(username) && ecosystem.getCustomerDirectory().removeCustomer(name)) {
                populate();
                JOptionPane.showMessageDialog(null, "Customer deleted successfully.");                 
            } else {
                JOptionPane.showMessageDialog(null, "Customer doesn't exist");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


