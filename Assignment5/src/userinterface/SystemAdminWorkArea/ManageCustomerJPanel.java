    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tableCustomers.getModel();
        dtm.setRowCount(0);
        
        for (Customer cus: ecosystem.getCustomerDirectory().getCustomerList()) {            
            Object[] row = new Object[4];
            row[0] = cus.getName();
            row[1] = cus.getUsername();
            row[2] = cus.getPassword();
            row[3] = cus.getPhone();
            dtm.addRow(row);
            
        }
    }

