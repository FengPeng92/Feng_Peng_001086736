    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableCustomers.getSelectedRow();
        
        if (selectedRow >= 0) {
            String name = (String)tableCustomers.getValueAt(selectedRow, 0);
            Customer curCus = ecosystem.getCustomerDirectory().searchCustomerByName(name);
            ViewCustomerDetailJPanel viewCustomerDetailJPanel = new ViewCustomerDetailJPanel(userProcessContainer, ecosystem, curCus);
            userProcessContainer.add("ViewCustomerDetailJPanel",viewCustomerDetailJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            System.out.println(curCus.getUsername());
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnViewActionPerformed

