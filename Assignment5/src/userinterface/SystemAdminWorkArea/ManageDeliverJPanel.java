    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableDeliverMan.getSelectedRow();

        if (selectedRow >= 0) {
            String name = (String)tableDeliverMan.getValueAt(selectedRow, 0);
            DeliveryMan curDeliveryMan = ecosystem.getDeliveryManDirectory().searchDeliveryManByName(name);
            ViewDeliveryManDetailJPanel viewDeliveryManDetailJPanel = new ViewDeliveryManDetailJPanel(userProcessContainer, ecosystem, curDeliveryMan);
            userProcessContainer.add("ViewDeliveryManDetailJPanel",viewDeliveryManDetailJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableDeliverMan.getSelectedRow();

        if (selectedRow >= 0) {
            String name = (String) tableDeliverMan.getValueAt(selectedRow, 0);
            String username = (String) tableDeliverMan.getValueAt(selectedRow, 1);

            if (ecosystem.getDeliveryManDirectory().removeDeliveryManByName(name) && ecosystem.getUserAccountDirectory().remvoveUserAccountbyName(username)) {
                populate();
                JOptionPane.showMessageDialog(null, "Delivery Man deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Delivery man doesn't exist");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


