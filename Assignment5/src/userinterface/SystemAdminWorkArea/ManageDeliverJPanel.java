    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tableDeliverMan.getModel();
        dtm.setRowCount(0);
        System.out.println(ecosystem.getDeliveryManDirectory().getDeliveryManList().size());
        
        for (DeliveryMan dm : ecosystem.getDeliveryManDirectory().getDeliveryManList()) {            
            Object[] row = new Object[4];
            row[0] = dm.getName();
            row[1] = dm.getUsername();
            row[2] = dm.getPassword();
            row[3] = dm.getPhone();
            dtm.addRow(row);
            
        }
    }
    

