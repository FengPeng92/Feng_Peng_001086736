    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tableMenu.getModel();
        dtm.setRowCount(0);
        
        for (MenuItem menu : restaurant.getMenus()) {
            Object[] row = new Object[3];
            row[0] = menu.getDishName();
            row[1] = menu.getDescription();
            row[2] = menu.getPrice();
            dtm.addRow(row);
        }
    }

