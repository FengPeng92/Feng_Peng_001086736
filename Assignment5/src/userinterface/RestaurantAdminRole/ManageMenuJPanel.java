    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableMenu.getSelectedRow();
        
        if (selectedRow >= 0) {
            String dishName = (String)tableMenu.getValueAt(selectedRow, 0);
            MenuItem selectedDish = restaurant.searchMenubyName(dishName);
            ViewDishDetailJPanel viewDishDetailJPanel = new ViewDishDetailJPanel(userProcessContainer, ecosystem, selectedDish);
            userProcessContainer.add("ViewDishDetailJPanel",viewDishDetailJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableMenu.getSelectedRow();
        
        if (selectedRow >= 0) {
            String dishName = (String)tableMenu.getValueAt(selectedRow, 0);
            if (restaurant.removeDishByName(dishName)) {
                JOptionPane.showMessageDialog(null, "remove successfully!");
                populate();
            } else {
                JOptionPane.showMessageDialog(null, "Dish doesn't exist");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed


